defmodule Aoc2024.Solutions.Y24.Day13Test do
  alias AoC.Input, warn: false
  alias Aoc2024.Solutions.Y24.Day13, as: Solution, warn: false
  use ExUnit.Case, async: true

  # To run the test, run one of the following commands:
  #
  #     mix AoC.test --year 2024 --day 13
  #
  #     mix test test/2024/day13_test.exs
  #
  # To run the solution
  #
  #     mix AoC.run --year 2024 --day 13 --part 1
  #
  # Use sample input file:
  #
  #     # returns {:ok, "priv/input/2024/day-13-mysuffix.inp"}
  #     {:ok, path} = Input.resolve(2024, 13, "mysuffix")
  #
  # Good luck!

  defp solve(input, part) do
    problem =
      input
      |> Input.as_file()
      |> Solution.parse(part)

    apply(Solution, part, [problem])
  end

  test "part one example" do
    input = ~S"""
    Button A: X+94, Y+34
    Button B: X+22, Y+67
    Prize: X=8400, Y=5400

    Button A: X+26, Y+66
    Button B: X+67, Y+21
    Prize: X=12748, Y=12176

    Button A: X+17, Y+86
    Button B: X+84, Y+37
    Prize: X=7870, Y=6450

    Button A: X+69, Y+23
    Button B: X+27, Y+71
    Prize: X=18641, Y=10279
    """

    assert 480 == solve(input, :part_one)
  end

